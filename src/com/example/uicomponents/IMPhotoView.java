package com.example.uicomponents;

import android.content.Context;
import android.util.AttributeSet;

import com.wuba.bangbang.uicomponents.photoview.PhotoView;

/**
 * 自定义的PhotoView
 * @author 徐宁
 * @date: 2015-2-27 上午10:53:22
 */
public class IMPhotoView extends PhotoView {

    /**
     * Creates a new instance of IMPhotoView.
     * 
     * @param context
     */
    public IMPhotoView(Context context) {
        super(context);
    }

    /**
     * Creates a new instance of IMPhotoView.
     * 
     * @param context
     * @param attr
     */
    public IMPhotoView(Context context, AttributeSet attr) {
        super(context, attr);
    }

    /**
     * Creates a new instance of IMPhotoView.
     * 
     * @param context
     * @param attr
     * @param defStyle
     */
    public IMPhotoView(Context context, AttributeSet attr, int defStyle) {
        super(context, attr, defStyle);
    }

}
